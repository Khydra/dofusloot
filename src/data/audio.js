export function playSound(sound, origin) {
	let s;

	if (origin == 'ui') s = soundUi[sound];
	if (origin == 'sfx') s = soundEffects[sound];
	if (origin == 'music') s = soundMusic[sound];

	s.pause();
	s.currentTime = 0;
	var nopromise = { catch : new Function() };
	(s.play() || nopromise).catch(function(){}); ;
}

export function setVolume() {
	let volUi = JSON.parse(window.localStorage.getItem("settings")).ui;
	let volMusic = JSON.parse(window.localStorage.getItem("settings")).music;
	let volEffects = JSON.parse(window.localStorage.getItem("settings")).effects;
	
	Object.keys(soundUi).forEach((key)=> soundUi[key].volume = volUi);
	Object.keys(soundMusic).forEach((key)=> soundMusic[key].volume = volMusic);
	Object.keys(soundEffects).forEach((key)=> soundEffects[key].volume = volEffects);
}
  